using System;

namespace Topic_16_Extension_methods___TimeSpan_
{
	class Program
	{
		static void Main(string[] args)
		{
			int a = 10;
			Console.WriteLine($"a.Seconds() = {a.Seconds()}");
			Console.WriteLine($"a.Minutes() = {a.Minutes()}");
			Console.WriteLine($"a.Hours() = {a.Hours()}");
		}
		
	}
}
